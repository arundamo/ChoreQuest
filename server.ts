import express from "express";
import path from "path";
import { createServer as createViteServer } from "vite";
import { GoogleGenAI, Type } from "@google/genai";
import dotenv from "dotenv";
import fs from "fs";
import { Firestore } from "@google-cloud/firestore";

dotenv.config();

const app = express();
const PORT = 3000;

app.use(express.json());

// Initialize Gemini Client Lazily to prevent crash on startup if key is missing
let aiClient: GoogleGenAI | null = null;
function getGeminiClient(): GoogleGenAI {
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey || apiKey === "MY_GEMINI_API_KEY") {
    throw new Error("GEMINI_API_KEY environment variable is not configured. Please add it to your project secrets.");
  }
  if (!aiClient) {
    aiClient = new GoogleGenAI({
      apiKey: apiKey,
      httpOptions: {
        headers: {
          'User-Agent': 'aistudio-build',
        }
      }
    });
  }
  return aiClient;
}

// Ensure clean error response helper
function sendError(res: express.Response, error: any) {
  console.error("AI Error:", error);
  res.status(500).json({ 
    error: error instanceof Error ? error.message : "An unexpected server-side error occurred." 
  });
}

// 1. Suggest Chores API
app.post("/api/suggest-chores", async (req, res) => {
  try {
    const age = req.body.age || 8;
    const ai = getGeminiClient();
    
    const prompt = `Suggest 5 age-appropriate chores for a ${age} year old child. For each chore provide: name, brief description, and suggested points value between 10 and 100. Return as JSON array with fields: name, description, points`;
    
    const response = await ai.models.generateContent({
      model: "gemini-3.5-flash",
      contents: prompt,
      config: {
        responseMimeType: "application/json",
        responseSchema: {
          type: Type.ARRAY,
          items: {
            type: Type.OBJECT,
            properties: {
              name: {
                type: Type.STRING,
                description: "The title of the chore; keep it short and action-oriented."
              },
              description: {
                type: Type.STRING,
                description: "A very brief, simple description of what needs to be done."
              },
              points: {
                type: Type.INTEGER,
                description: "Points reward value between 10 and 100 based on standard difficulty."
              }
            },
            required: ["name", "description", "points"]
          }
        }
      }
    });

    const text = response.text;
    if (!text) {
      return res.status(500).json({ error: "No response text generated by AI." });
    }
    const data = JSON.parse(text);
    res.json(data);
  } catch (error) {
    sendError(res, error);
  }
});

