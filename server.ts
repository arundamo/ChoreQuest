const DATA_FILE = path.join(process.cwd(), "family-data.json");

// Lazy load Firestore client to keep startup safe and fast
let dbClient: Firestore | null = null;
function getFirestoreDb(): Firestore {
  if (!dbClient) {
    dbClient = new Firestore({
      projectId: "gen-lang-client-0834237355",
      databaseId: "ai-studio-chorequest-ee1e3543-2f2a-4e12-a4fd-52aacae6d3d9"
    });
  }
  return dbClient;
}

// 4. Family Data - Get synchronization state fallback
app.get("/api/family-data", (req, res) => {
  try {
    if (fs.existsSync(DATA_FILE)) {
      const raw = fs.readFileSync(DATA_FILE, "utf-8");
      const parsed = JSON.parse(raw);
      return res.json({ initialized: true, ...parsed });
    }
    return res.json({ initialized: false });
  } catch (error) {
    console.error("Error reading family data from fallback file:", error);
    res.status(500).json({ error: "Failed to read family data from local backup" });
  }
});

// 5. Family Data - Update synchronization state fallback
app.post("/api/family-data", (req, res) => {
  try {
    const data = req.body;
    fs.writeFileSync(DATA_FILE, JSON.stringify(data, null, 2), "utf-8");
    res.json({ success: true });
  } catch (error) {
    console.error("Error saving family data to fallback file:", error);
    res.status(500).json({ error: "Failed to save family data to local backup" });
  }
});

// Vite Middleware integration for development / production serving
async function startServer() {
  if (process.env.NODE_ENV !== "production") {
    console.log("Starting server in DEVELOPMENT mode with Vite integration...");
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    console.log("Starting server in PRODUCTION mode...");
    const distPath = path.join(process.cwd(), "dist");
    app.use(express.static(distPath));
    // SPA fallback
    app.get("*", (req, res) => {
      res.sendFile(path.join(distPath, "index.html"));
    });
  }

  app.listen(PORT, "0.0.0.0", () => {
    console.log(`ChoreQuest server is listening on http://0.0.0.0:${PORT}`);
  });
}

startServer().catch((err) => {
  console.error("Critical: Failed to start server:", err);
});
